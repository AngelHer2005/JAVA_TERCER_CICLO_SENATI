    private void itemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemsActionPerformed
        // TODO add your handling code here:
        double[] precios = {3.50, 9.00, 4.00, 3.20};
        for (int i = 0; i < items.getItemCount(); i++) {
            codigos.put(items.getItemAt(i), "C00"+Integer.toString(i+1));
        }
        ArrayList<String> lista = new ArrayList<>(codigos.keySet());

        cod.setText(codigos.get(items.getSelectedItem().toString()));
        if("Mostaza".equals(items.getSelectedItem().toString())){
            preco.setText("S/. " + Double.toString(precios[3]));
        }else{
            preco.setText("S/. " + Double.toString(precios[lista.indexOf(items.getSelectedItem().toString())-1]));
        }
    }//GEN-LAST:event_itemsActionPerformed

    private void calActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calActionPerformed
        // TODO add your handling code here:
        double val=0;
        if ("Simple".equals(tipo.getSelectedItem().toString())) {
            val=1.35;
        }else if ("Complejo".equals(tipo.getSelectedItem().toString())) {
            val=1.65;
        }else{
            val=1.95;
        }
        double resultado = Double.parseDouble(preco.getText().substring(4))*val;
        double preventa = (double) Math.round(resultado * 100.0) / 100.0;
        preve.setText("S/. " + preventa);
    }//GEN-LAST:event_calActionPerformed

